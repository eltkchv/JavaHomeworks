package com.company;

public class Params {
    private final double a;
    private final double b;
    private final double r;

    public Params(double a, double b, double r) {
        this.a = a;
        this.b = b;
        this.r = r;
    }

    public double getA() {
        return a;
    }

    public double getB() {
        return b;
    }

    public double getR() {
        return r;
    }
}